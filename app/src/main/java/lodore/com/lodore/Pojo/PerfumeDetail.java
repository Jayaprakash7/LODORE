public class PerfumeDetail {
    private String _id;
    private String name;
    private String parent;
    private String image;
    private String description;
    private String size;
    private String concentration;
    private String perfume_type;
    private String occasion;
    private String perfume_family;
    private String price;
    private String perfume_story;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getParent() {
        return parent;
    }

    public void setParent(String parent) {
        this.parent = parent;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getConcentration() {
        return concentration;
    }

    public void setConcentration(String concentration) {
        this.concentration = concentration;
    }

    public String getPerfume_type() {
        return perfume_type;
    }

    public void setPerfume_type(String perfume_type) {
        this.perfume_type = perfume_type;
    }

    public String getOccasion() {
        return occasion;
    }

    public void setOccasion(String occasion) {
        this.occasion = occasion;
    }

    public String getPerfume_family() {
        return perfume_family;
    }

    public void setPerfume_family(String perfume_family) {
        this.perfume_family = perfume_family;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPerfume_story() {
        return perfume_story;
    }

    public void setPerfume_story(String perfume_story) {
        this.perfume_story = perfume_story;
    }
}
